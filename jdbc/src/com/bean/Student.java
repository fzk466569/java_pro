package com.bean;

public class Student {
	private int id;
	private String name;
	private int age;
	private int gander;
	private String type;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getGander() {
		return gander;
	}
	public void setGander(int gander) {
		this.gander = gander;
	}

}
